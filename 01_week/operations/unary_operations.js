/************** UNARY OPERATOR ***************/
console.log('UNARY OPERATOR');

// Pre-increment (Change, then use)
let preAge = 55;
console.log(++preAge);


//Post-increment (Use, then change)
let postAge = 55;
console.log(postAge++);
console.log(postAge);


// Pre-Decrement (Change, then use)
let pre_age = 55;
console.log(--pre_age);


//Post-Decrement (Use, then change)
let post_newAge = 55;
console.log(post_newAge--);
console.log(post_newAge);